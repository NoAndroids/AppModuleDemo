public class NewslvAdapter extends BaseAdapter {
    private Context context;
    private List<News.NewslistBean> data;

    public NewslvAdapter(Context context, List<News.NewslistBean> data) {
        this.context = context;
        this.data = data;
    }
    public  void setNewData( List<News.NewslistBean> data){
        this.data=data;
        notifyDataSetChanged();
    }

    @Override
    public int getCount() {
        return data == null ? 0 : data.size();
    }

    @Override
    public Object getItem(int position) {
        return data.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        ViewHolder viewHolder=null;
        if (convertView == null) {
            convertView = LayoutInflater.from(context).inflate(R.layout.item_lv, null);
            viewHolder=new ViewHolder(convertView);
            convertView.setTag(viewHolder);
        }else {
            viewHolder= (ViewHolder) convertView.getTag();
        }
        viewHolder.tvCtime.setText(data.get(position).getCtime());
        viewHolder.tvTitle.setText(data.get(position).getTitle());
     //   convertView.setTag(position,data.get(position).getUrl());

        return convertView;
    }

    static class ViewHolder {
        @BindView(R.id.iv)
        ImageView iv;
        @BindView(R.id.tv_title)
        TextView tvTitle;
        @BindView(R.id.tv_ctime)
        TextView tvCtime;

        ViewHolder(View view) {
            ButterKnife.bind(this, view);
        }
    }
}
